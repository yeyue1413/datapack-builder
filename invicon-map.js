export function getInviconUrl(itemId) {
    if (!itemId || typeof itemId !== 'string') return '';
    const file = INVICON_MAP[itemId];
    if (file) return 'invicon/' + file;
    return '';
}

export function getInviconHtml(itemId, altText, extraClass) {
    altText = altText || '';
    extraClass = extraClass || '';
    const url = getInviconUrl(itemId);
    if (!url) return '';
    const cls = ('invicon-img ' + extraClass).trim();
    const alt = altText || itemId || '';
    return '<img src="' + url + '" alt="' + alt + '" class="' + cls + '" loading="lazy" onerror="this.style.display=' + "'none'" + '">';
}